local function printAsciiArt(text)
    text = string.upper(text)
    for line = 1, 7 do
        local output = ""
        for i = 1, #text do
            local char = string.sub(text, i, i)
            if not ascii_chars[char] then
                i = i + 1;
            end
            if ascii_chars[char] then
                output = output .. ascii_chars[char][line] .. " "
            end
        end
        print(output)
    end
end

return printAsciiArt
